SELECT Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД  FROM Н_ГРУППЫ_ПЛАНОВ
JOIN Н_ПЛАНЫ ON  Н_ПЛАНЫ.ПЛАН_ИД=Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
WHERE Н_ПЛАНЫ.ФО_ИД=(SELECT ИД FROM Н_ФОРМЫ_ОБУЧЕНИЯ
WHERE ИМЯ_В_ИМИН_ПАДЕЖЕ='заочная')
GROUP BY Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
HAVING COUNT(*)=2;

SELECT Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД FROM Н_ГРУППЫ_ПЛАНОВ
JOIN Н_ПЛАНЫ ON  Н_ПЛАНЫ.ПЛАН_ИД=Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
WHERE Н_ПЛАНЫ.ФО_ИД=(SELECT ИД FROM Н_ФОРМЫ_ОБУЧЕНИЯ
WHERE ИМЯ_В_ИМИН_ПАДЕЖЕ='очная')
GROUP BY Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
HAVING COUNT(*)>2;

