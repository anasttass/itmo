SELECT s.ЧЛВК_ИД, s.ГРУППА, CONCAT(l.ФАМИЛИЯ,' ',l.ИМЯ,' ',l.ОТЧЕСТВО) as ФИО, AVG(v.ОЦЕНКА::integer)FROM Н_УЧЕНИКИ s
JOIN Н_ЛЮДИ l ON l.ИД=s.ЧЛВК_ИД
JOIN Н_ВЕДОМОСТИ v ON s.ЧЛВК_ИД=v.ЧЛВК_ИД
WHERE s.ГРУППА = '4100' AND v.ОЦЕНКА~'^\d+$'
GROUP BY s.ЧЛВК_ИД, s.ГРУППА, l.ФАМИЛИЯ, l.ИМЯ, l.ОТЧЕСТВО 
HAVING AVG(v.ОЦЕНКА::integer)>=
(SELECT AVG(nv.ОЦЕНКА::integer) FROM Н_УЧЕНИКИ ns
JOIN Н_ВЕДОМОСТИ nv ON ns.ЧЛВК_ИД=nv.ЧЛВК_ИД
WHERE ns.ГРУППА='1101' AND nv.ОЦЕНКА~'^\d+$'
);

