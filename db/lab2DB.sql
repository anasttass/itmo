SELECT Н_УЧЕНИКИ.ГРУППА, Н_УЧЕНИКИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ,
Н_ЛЮДИ.ОТЧЕСТВО, Н_ПЛАНЫ.НОМЕР, Н_УЧЕНИКИ.СОСТОЯНИЕ FROM Н_УЧЕНИКИ
JOIN Н_ЛЮДИ ON Н_ЛЮДИ.ИД=Н_УЧЕНИКИ.ЧЛВК_ИД
JOIN Н_ПЛАНЫ ON Н_УЧЕНИКИ.ПЛАН_ИД=Н_ПЛАНЫ.ИД
WHERE Н_ПЛАНЫ.ФО_ИД=(SELECT ИД FROM Н_ФОРМЫ_ОБУЧЕНИЯ
WHERE ИМЯ_В_ИМИН_ПАДЕЖЕ='заочная')
AND date(Н_ПЛАНЫ.ДАТА_УТВЕРЖДЕНИЯ)<'01.09.2012' AND Н_ПЛАНЫ.КУРС=1
GROUP BY Н_УЧЕНИКИ.ГРУППА, Н_УЧЕНИКИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ,
Н_ЛЮДИ.ОТЧЕСТВО, Н_ПЛАНЫ.НОМЕР, Н_УЧЕНИКИ.СОСТОЯНИЕ;

SELECT Н_УЧЕНИКИ.ГРУППА, Н_УЧЕНИКИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ,
Н_ЛЮДИ.ОТЧЕСТВО, Н_ПЛАНЫ.НОМЕР, Н_УЧЕНИКИ.СОСТОЯНИЕ FROM Н_УЧЕНИКИ
JOIN Н_ЛЮДИ ON Н_ЛЮДИ.ИД=Н_УЧЕНИКИ.ЧЛВК_ИД
